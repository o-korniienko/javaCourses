package les_;

public class TestNumeralSystems {
    public static void main(String[] args) {
        int a16 = 0xF;
        System.out.println("a16 = " + a16); //15

        int a8 = 011;
        System.out.println("a8 = " + a8);

        int a2 = 0b111;
        System.out.println("a2 = " + a2);
    }
}
